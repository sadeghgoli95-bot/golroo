/** Single XML-escaping implementation for every feed route — avoids malformed/injectable XML from Persian article text containing &, <, >, quotes. */
export function escapeXml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}
